$(document).ready(function(){
    $(".수업").mouseover(function(){
        var image = $(".수업i");
    $(".수업i").show();
    $(".수업i").stop().animate({top:window.innerHeight/2-image.innerHeight()/2});
    });
    $(".수업").mouseout(function(){
    $(".수업i").stop().animate({top:"100%"});
    });
    });

